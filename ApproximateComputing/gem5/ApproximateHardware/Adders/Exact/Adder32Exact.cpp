static unsigned int binarytodecimal(int binary[outsize]){
      unsigned int c, d;
      unsigned int result=0;
      for (c = 0 ; c < outsize ; c++){
        d = 1 << c;
        result = result + d*binary[c];
      }
      return result;
 }
